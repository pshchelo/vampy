"""
Collection of base64-encoded icons and tools for them.
Provides the following:
    -Data:
        iconsdict - dictionary containing lists of images encoded as base64-strings;
    - Functions:
        GetIcon(string) - takes base64'd image and returns it as wx.Icon;
        GetIconBundle(icontitle) - looks up the icontitle in iconsdict and returns all
            images in the corresponding list as wx.IconBundle
            for use with wxTopLevelWindow.SetIcons();
This module is only for import.
"""
import wx
from base64 import b64decode
from StringIO import StringIO

def _GetIcon(icon_b64):
    x = StringIO(b64decode(icon_b64))
    icon = wx.ImageFromStream(x)
    x.close()
    return wx.IconFromBitmap(wx.BitmapFromImage(icon))

def GetIconBundle(icontitle):
    ib = wx.IconBundle()
    if icontitle in _iconsdict:
        for b64string in _iconsdict[icontitle]:
            ib.AddIcon(_GetIcon(b64string))
    return ib

